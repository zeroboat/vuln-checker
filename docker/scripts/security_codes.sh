# 카테고리 매핑
get_docker_category() {
    local code="$1"
    local num="${code#D-}"
    num=$((10#$num))
    if   [ "$num" -ge 1  ] && [ "$num" -le 7  ]; then echo "호스트 설정"
    elif [ "$num" -ge 8  ] && [ "$num" -le 25 ]; then echo "데몬 설정"
    elif [ "$num" -ge 26 ] && [ "$num" -le 37 ]; then echo "설정 파일 권한"
    elif [ "$num" -ge 38 ] && [ "$num" -le 47 ]; then echo "이미지 및 빌드"
    elif [ "$num" -ge 48 ] && [ "$num" -le 62 ]; then echo "컨테이너 런타임"
    elif [ "$num" -ge 63 ] && [ "$num" -le 65 ]; then echo "보안 운영"
    else                                               echo "Swarm 설정"
    fi
}
