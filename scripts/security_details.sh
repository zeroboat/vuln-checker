# 보안 항목 상세 정보 출력 함수
print_security_detail() {
    local code="$1"
    
    local purpose="${SECURITY_DETAILS[${code}_PURPOSE]}"
    local check="${SECURITY_DETAILS[${code}_CHECK]}"
    local good="${SECURITY_DETAILS[${code}_GOOD]}"
    local bad="${SECURITY_DETAILS[${code}_BAD]}"
    local action="${SECURITY_DETAILS[${code}_ACTION]}"
    local threat="${SECURITY_DETAILS[${code}_THREAT]}"
    
    if [ -n "$purpose" ]; then
        append_log ""
        append_log "  [ 점검 목적 ]"
        append_log "  ${purpose}"
        append_log ""
        append_log "  [ 점검 내용 ]"
        append_log "  ${check}"
        append_log ""
        append_log "  [ 보안 위협 ]"
        append_log "  ${threat}"
        append_log ""
        append_log "  [ 판단 기준 ]"
        append_log "  양호: ${good}"
        append_log "  취약: ${bad}"
        append_log ""
        append_log "  [ 조치 방법 ]"
        append_log "  ${action}"
    fi
}

